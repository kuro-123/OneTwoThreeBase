package host.kuro.onetwothree.datatype;

public class ItemInfo {
    public int id;
    public String name;
    public int price;
    public String ban;

    public ItemInfo(int id, String name, int price, String ban) {
        this.id = id;
        this.name = name;
        this.price = price;
        this.ban = ban;
    }
}
